#include "philo_bonus.h"

int	ft_is_uint(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] == '0')
		i++;
	j = i;
	while (str[i] >= '0' && str[i] <= '9')
		i++;
	return (!str[i] && (i - j) < 11 && ft_atoi(str) > 0);
}
